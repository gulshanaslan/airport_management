    private void SaveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveBtnMouseClicked
        // TODO add your handling code here:
        try {
            Flight flight = new Flight();
            Airport depAirport = new Airport();
            Airport arrAirport = new Airport();
            Plane plane = new Plane();
            Company company = new Company();
            Item depAirportItem = (Item) DepAirportCombo.getSelectedItem();
            Item arrAirportItem = (Item) ArrAirportCombo.getSelectedItem();
            Item planeItem = (Item) PlaneCombo.getSelectedItem();
            Item companyİtem = (Item) CompanyCombo.getSelectedItem();
            flight.setId(selectedId);
            flight.setFlight_code(Long.parseLong(FlightNoTxt.getText()));
            depAirport.setId(depAirportItem.getId());
            arrAirport.setId(arrAirportItem.getId());
            flight.setDeparture_airport(depAirport);
            flight.setArrival_airport(arrAirport);
            company.setId(companyİtem.getId());
            flight.setCompany(company);
            plane.setId(planeItem.getId());
            flight.setPlane(plane);
            java.util.Date depd = DepDate.getDate();
            java.sql.Date depsqldate = new java.sql.Date(depd.getTime());
            flight.setDeparture_date(depsqldate);
            java.util.Date arrd = ArrDate.getDate();
            java.sql.Date arrsqldate = new java.sql.Date(arrd.getTime());
            flight.setArrival_date(arrsqldate);
            //flight.setArrival_date(  ArrDate.getDate());
            flight.setActive(1);

            boolean isAdded = flightDao.updateFlight(flight);
            if (isAdded) {
                JOptionPane.showMessageDialog(null, "Flight has been added", "Add Flight", HEIGHT);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error", "Add Flight", HEIGHT);
            }
        } catch (Exception ex) {
            Logger.getLogger(UpdateFlight.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }


    }//GEN-LAST:event_SaveBtnMouseClicked

