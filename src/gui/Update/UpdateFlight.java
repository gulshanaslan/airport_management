 private void DepAirportCombo() {
        try {
            DefaultComboBoxModel model = (DefaultComboBoxModel) DepAirportCombo.getModel();
            Flight selectedFlight = flightDao.getFlightById(selectedId);
            List<Airport> airports = airportDao.getAirportList();
            for (Airport airport : airports) {
                model.addElement(new Item(airport.getId(), airport.getName()));
            }
            model.setSelectedItem(new Item(selectedFlight.getDeparture_airport().getId(), selectedFlight.getDeparture_airport().getName()));
            FlightNoTxt.setText(String.valueOf(selectedFlight.getFlight_code()));
            DepDate.setDate(selectedFlight.getDeparture_date());
            ArrDate.setDate(selectedFlight.getArrival_date());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void ArrAirportCombo() {
        try {
            DefaultComboBoxModel model = (DefaultComboBoxModel) ArrAirportCombo.getModel();
            Flight selectedFlight = flightDao.getFlightById(selectedId);
            List<Airport> airports = airportDao.getAirportList();
            for (Airport airport : airports) {
                model.addElement(new Item(airport.getId(), airport.getName()));
            }
            model.setSelectedItem(new Item(selectedFlight.getArrival_airport().getId(), selectedFlight.getArrival_airport().getName()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void PlaneCombo() {
        try {
            DefaultComboBoxModel model = (DefaultComboBoxModel) PlaneCombo.getModel();
            Flight selectedFlight = flightDao.getFlightById(selectedId);
            List<Plane> planes = planeDao.getPlaneList();
            for (Plane plane : planes) {
                model.addElement(new Item(plane.getId(), plane.getName()));
            }
            model.setSelectedItem(new Item(selectedFlight.getPlane().getId(), selectedFlight.getPlane().getName()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void CompanyCombo() {
        try {
            DefaultComboBoxModel model = (DefaultComboBoxModel) CompanyCombo.getModel();
            Flight selectedFlight = flightDao.getFlightById(selectedId);
            List<Company> companys = companyDao.getCompanyList();
            for (Company company : companys) {
                model.addElement(new Item(company.getId(), company.getCompany_name()));
            }
            model.setSelectedItem(new Item(selectedFlight.getCompany().getId(), selectedFlight.getCompany().getCompany_name()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

