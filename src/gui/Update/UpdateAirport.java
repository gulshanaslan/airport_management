private void CountryCombo() {
        try {
            DefaultComboBoxModel model = (DefaultComboBoxModel) CountryCombo.getModel();
            List<Country> countrys = countryDao.getCountryList();
            for (Country country : countrys) {
                model.addElement(new Item(country.getId(), String.valueOf(country.getName())));
            }
            model.setSelectedItem(new Item(selectedAirport.getCountry().getId(), selectedAirport.getCountry().getName()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

