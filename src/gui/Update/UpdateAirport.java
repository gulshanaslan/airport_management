    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Airport airport = new Airport();
            Item item = (Item) CountryCombo.getSelectedItem();
            airport.setId(selectedAirport.getId());
            airport.setName(NameTxt.getText());
            Country country = new Country();
            country.setId(item.getId());
            airport.setCountry(country);
            airport.setActive(1);
            boolean isAdded = airportDao.updateAirport(airport);
            if (isAdded) {
                JOptionPane.showMessageDialog(null, "Passenger has been added", "Add Passenger", HEIGHT);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error", "Add Passenger", HEIGHT);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error", "Add Passenger", HEIGHT);
            Logger.getLogger(UpdateAirport.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

