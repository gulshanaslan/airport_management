  private void FlightCombo() {
        try {
            DefaultComboBoxModel model = (DefaultComboBoxModel) FlightCombo.getModel();
            List<Flight> flights = flightDao.getFlightList();
            for (Flight flight : flights) {
                model.addElement(new Item(flight.getId(), String.valueOf(flight.getFlight_code())));
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void PassengerCombo() {
        try {
            DefaultComboBoxModel model = (DefaultComboBoxModel) PassengerCombo.getModel();
            List<Passenger> passengers = passengerDao.getPassengerList();
            for (Passenger passenger : passengers) {
                model.addElement(new Item(passenger.getId(), passenger.getName() + " " + passenger.getSurname() + " " + passenger.getPassportId()));
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
