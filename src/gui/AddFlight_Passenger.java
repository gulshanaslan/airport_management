    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Flight_Passenger flight_Passenger = new Flight_Passenger();
            Flight flight = new Flight();
            Item flightItem = (Item) FlightCombo.getSelectedItem();
            Item passengerItem = (Item) PassengerCombo.getSelectedItem();
            Passenger passenger = new Passenger();
            flight_Passenger.setId(Long.parseLong(IdTxt.getText()));
            flight.setId(flightItem.getId());
            flight_Passenger.setFlight(flight);
            passenger.setId(passengerItem.getId());
            flight_Passenger.setActive(1);
            flight_Passenger.setPassenger(passenger);
            boolean isAdded = flight_PassengerDao.addFlight_Passenger(flight_Passenger);
            if (isAdded) {
                JOptionPane.showMessageDialog(null, "Flight_Passsenger has been added", "Add Group", HEIGHT);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error", "Flight_Passenger Group", HEIGHT);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error", "Flight_Passenger Group", HEIGHT);
            Logger.getLogger(AddFlight_Passenger.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

